#!/usr/bin/env node
// Reads the .md fragments next to src/durable/agent-prompt/index.ts and
// emits inline.generated.ts as a plain TS module exporting each fragment
// as a string constant. Re-run after editing any of the .md files.
//
// This indirection exists because OpenNext's esbuild pass for the worker
// doesn't honor Next's webpack loader config, so a direct `.md` import
// works in dev but breaks at deploy time. A generated .ts file is the
// portable answer.

import fs from 'node:fs'
import path from 'node:path'
import url from 'node:url'

const here = path.dirname(url.fileURLToPath(import.meta.url))
const root = path.resolve(here, '..')
const promptDir = path.join(root, 'src/durable/agent-prompt')

const FRAGMENTS = [
  { file: 'beancount-primer.md', name: 'BEANCOUNT_PRIMER' },
  { file: 'tool-rules.md', name: 'TOOL_RULES' },
  { file: 'examples.md', name: 'EXAMPLES' },
  { file: 'clarifications.md', name: 'CLARIFICATIONS' },
  { file: 'statement-handling.md', name: 'STATEMENT_HANDLING' },
  { file: 'statement-extraction.md', name: 'STATEMENT_EXTRACTION' },
  { file: 'analyst.md', name: 'ANALYST_ROLE' },
]

const banner = `// AUTO-GENERATED by scripts/build-agent-prompt.mjs from the .md files in
// this folder. Do not edit by hand — edit the .md files and re-run the
// script (it runs automatically before \`pnpm build\` / \`pnpm deploy\`).
`

const body = FRAGMENTS.map(({ file, name }) => {
  const text = fs.readFileSync(path.join(promptDir, file), 'utf8').replace(/\s+$/, '')
  return `export const ${name} = ${JSON.stringify(text)}`
}).join('\n\n')

const outPath = path.join(promptDir, 'inline.generated.ts')
const out = `${banner}\n${body}\n`

const prev = fs.existsSync(outPath) ? fs.readFileSync(outPath, 'utf8') : null
if (prev === out) {
  process.stdout.write(`agent-prompt: inline.generated.ts already up to date\n`)
} else {
  fs.writeFileSync(outPath, out)
  process.stdout.write(`agent-prompt: wrote inline.generated.ts (${out.length} bytes)\n`)
}
