// Generate the bundled airport reference dataset for AirportsDO from our own
// committed KG airport files (content/airports/*.md). Build-time only, no
// external calls; the runtime DO is seeded from the emitted file and never
// touches the KG. Each KG airport node is:
//
//   ::node[airport/bom]
//   # Chhatrapati Shivaji International Airport
//   ::
//
//   Chhatrapati Shivaji International Airport (BOM). Mumbai, India. Lat …, Long …
//
// → { iata, name, city, country }. Run: node scripts/gen-airports-data.mjs
import fs from 'node:fs'
import path from 'node:path'

const KG = path.resolve('../milesvault-kg/content/airports')
const OUT = path.resolve('src/durable/airports/airports-data.ts')

const block = /::node\[airport\/([a-z0-9]+)\]\n# ([^\n]+)\n::\n\n([^\n]+)/g

const rows = []
let unparsed = 0
for (const f of fs.readdirSync(KG).filter((n) => n.endsWith('.md'))) {
  const text = fs.readFileSync(path.join(KG, f), 'utf8')
  let m
  while ((m = block.exec(text)) !== null) {
    const iata = m[1].toUpperCase()
    const name = m[2].trim()
    const prose = m[3]
    // prose: "<name> (<IATA>). <City, >Country. <loc>" — take the segment after
    // "). " and before the location sentence ("Lat …" / "Coordinates …").
    const after = prose.split(/\)\.\s/)[1] ?? ''
    const place = after.split(/\.\s/)[0] ?? '' // "City, Country" or "Country"
    const parts = place.split(', ')
    const country = (parts.pop() ?? '').trim()
    const city = parts.join(', ').trim()
    if (!/^[A-Z]{3}$/.test(iata) || !name) {
      unparsed++
      continue
    }
    rows.push({ iata, name, city: city || null, country: country || null })
  }
}
rows.sort((a, b) => a.iata.localeCompare(b.iata))

const body = rows
  .map(
    (r) =>
      `  [${JSON.stringify(r.iata)},${JSON.stringify(r.name)},${JSON.stringify(r.city)},${JSON.stringify(r.country)}],`,
  )
  .join('\n')

const out = `// AUTO-GENERATED by scripts/gen-airports-data.mjs from the KG airport nodes.
// Do not edit by hand. ${rows.length} airports. Reference data seeded into
// AirportsDO (FTS5). [iata, name, city|null, country|null].
export type AirportRecord = readonly [iata: string, name: string, city: string | null, country: string | null]
export const AIRPORTS_DATA: readonly AirportRecord[] = [
${body}
]
`

fs.mkdirSync(path.dirname(OUT), { recursive: true })
fs.writeFileSync(OUT, out)
console.log(`airports: ${rows.length}, unparsed skipped: ${unparsed} → ${path.relative(process.cwd(), OUT)}`)
